"""Tests for ``lagrangian.operator_action.gauge_variation``.

These tests exercise the **infinitesimal** gauge variation operator on
already-lowered ``CompiledLagrangian`` objects:

* matter pieces (scalars and fermions) under U(1) and a small non-abelian
  group built by hand,
* the gauge-boson variation (homogeneous + inhomogeneous parts),
* pure Yang-Mills ``-1/4 F^2``,
* explicit "wrong charge / wrong representation" checks.

We verify gauge invariance by exporting ``apply_operator(delta).to_symbolica()``
and running it through the project's tensor-canonicaliser (which knows how
to relabel dummy spinor / Lorentz / adjoint indices and contract the
Minkowski metric). The exact dummy labels generated by lowering and by
``gauge_variation`` are passed in explicitly so the check is deterministic.
"""

from __future__ import annotations

import pytest

from symbolica import Expression, S

from model import (
    CovD,
    Field,
    FieldStrength,
    Gamma,
    GaugeGroup,
    GaugeRepresentation,
    LORENTZ_INDEX,
    Model,
    PartialD,
    WEAK_ADJ_INDEX,
    WEAK_FUND_INDEX,
    dirac_field,
    scalar_field,
)
from model.metadata import COLOR_ADJ_INDEX, COLOR_FUND_INDEX
from model.interactions import InteractionTerm
from lagrangian.operator_action import (
    FieldOperator,
    apply_field_operator_to_term,
    gauge_variation,
)
from symbolic.spenso_structures import (
    gauge_generator,
    structure_constant,
    weak_gauge_generator,
    weak_structure_constant,
)
from symbolic.tensor_canonicalization import canonize_full


def _zero(
    expr,
    *,
    lorentz=(),
    adjoint=(),
    color_fund=(),
    spinor=(),
    weak_fund=(),
    weak_adj=(),
    field_heads=(),
):
    """Canonicalise ``expr`` with the given dummy index symbols and return
    ``True`` iff the result is the symbolic zero.
    """
    canonical = canonize_full(
        expr,
        lorentz_indices=tuple(lorentz),
        adjoint_indices=tuple(adjoint),
        color_fund_indices=tuple(color_fund),
        spinor_indices=tuple(spinor),
        weak_fund_indices=tuple(weak_fund),
        weak_adj_indices=tuple(weak_adj),
        field_heads=tuple(field_heads),
    )
    return canonical.to_canonical_string() == "0"


