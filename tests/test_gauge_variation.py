def test_gauge_variation_returns_field_operator():
    """``gauge_variation`` returns a ``FieldOperator`` whose parity is even."""

    g = S("g")
    A = Field("A", spin=1, indices=(LORENTZ_INDEX,), self_conjugate=True)
    U1 = GaugeGroup(name="U1", abelian=True, coupling=g, charge="Q", gauge_boson=A)

    delta = gauge_variation(group=U1, parameter="alpha")
    assert isinstance(delta, FieldOperator)
    assert delta.parity == 0
    assert hasattr(delta, "_gauge_parameter_field")
    assert delta._gauge_parameter_field.name == "alpha"


def test_non_abelian_group_without_gauge_boson_raises():
    """Non-abelian gauge variation needs a gauge boson to read the adjoint
    index off; building it without one is a clear error.
    """

    g = S("g3")
    SU3 = GaugeGroup(
        name="SU3",
        abelian=False,
        coupling=g,
        gauge_boson=None,
        structure_constant=structure_constant,
        representations=(
            GaugeRepresentation(
                index=COLOR_FUND_INDEX,
                generator_builder=gauge_generator,
                name="fundamental",
            ),
        ),
    )
    with pytest.raises(ValueError, match="no gauge_boson"):
        gauge_variation(group=SU3, parameter="alpha")


