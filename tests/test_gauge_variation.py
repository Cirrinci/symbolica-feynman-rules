def test_non_abelian_scalar_mass_term_is_gauge_invariant():
    """For a complex scalar in the fundamental of SU(3), the colour-singlet
    mass ``m^2 Phibar Phi`` (with a contracted colour index) is gauge
    invariant: the two T^a contributions cancel because they enter
    Phi and Phibar with opposite signs and matching index structure.
    """

    g, Ag, SU3 = _su3_setup()
    c = S("c")
    phi = scalar_field(
        "Phi",
        self_conjugate=False,
        indices=(COLOR_FUND_INDEX,),
    )

    L = Model(
        gauge_groups=(SU3,),
        fields=(phi, Ag),
        lagrangian_decl=S("m2") * phi.bar(c) * phi(c),
    ).lagrangian()

    delta = gauge_variation(group=SU3, parameter="alpha")
    dL = L.apply_operator(delta)
    assert _zero(
        dL.to_symbolica(),
        color_fund=(c, S("c_delta_SU3_1"), S("c_delta_SU3_3")),
        adjoint=(S("a_delta_SU3_2"), S("a_delta_SU3_4")),
    )


def test_non_abelian_singlet_coupled_to_fundamental_pair_is_gauge_invariant():
    """A singlet ``S`` multiplied by a contracted colour pair
    ``Phi.bar(c) Phi(c)`` is still a colour singlet, so the variation must
    vanish: the operator only rotates ``Phi`` and ``Phi.bar``, and their
    contributions cancel exactly like in the bare mass term.

    This guards against the operator accidentally rotating the singlet
    or breaking the cancellation between the two fundamental factors.
    """

    g, Ag, SU3 = _su3_setup()
    phi = scalar_field(
        "Phi", self_conjugate=False, indices=(COLOR_FUND_INDEX,),
    )
    sing = scalar_field("S", self_conjugate=True)
    c = S("c")

    L = Model(
        gauge_groups=(SU3,),
        fields=(phi, sing, Ag),
        lagrangian_decl=S("y") * sing * phi(c) * phi.bar(c),
    ).lagrangian()

    delta = gauge_variation(group=SU3, parameter="alpha")
    dL = L.apply_operator(delta)
    assert _zero(
        dL.to_symbolica(),
        color_fund=(c, S("c_delta_SU3_1"), S("c_delta_SU3_3")),
        adjoint=(S("a_delta_SU3_2"), S("a_delta_SU3_4")),
    )


def test_non_abelian_non_singlet_is_not_gauge_invariant():
    """A non-singlet construction with free colour indices does *not*
    cancel under an SU(3) variation. We build ``y Phi(c1) Phi.bar(c2)``
    with **uncontracted** ``c1`` and ``c2``; the two T^a contributions
    have different colour structures and do not sum to zero.
    """

    g, Ag, SU3 = _su3_setup()
    phi = scalar_field(
        "Phi", self_conjugate=False, indices=(COLOR_FUND_INDEX,),
    )
    c1, c2 = S("c1"), S("c2")

    L = Model(
        gauge_groups=(SU3,),
        fields=(phi, Ag),
        lagrangian_decl=S("y") * phi(c1) * phi.bar(c2),
    ).lagrangian()

    delta = gauge_variation(group=SU3, parameter="alpha")
    dL = L.apply_operator(delta)
    canonical = canonize_full(
        dL.to_symbolica(),
        color_fund_indices=(
            c1, c2,
            S("c_delta_SU3_1"), S("c_delta_SU3_3"),
        ),
        adjoint_indices=(S("a_delta_SU3_2"), S("a_delta_SU3_4")),
    ).to_canonical_string()
    assert canonical != "0", f"expected nonzero variation but got 0: {canonical}"


def test_non_abelian_slot_policy_sum_emits_one_variation_per_active_slot():
    """Repeated active representation slots contribute in first-seen slot order."""

    g, Ag, SU3 = _su3_sum_setup()
    phi = scalar_field(
        "PhiBi",
        self_conjugate=False,
        indices=(COLOR_FUND_INDEX, COLOR_FUND_INDEX),
    )
    c1, c2 = S("c1"), S("c2")

    L = Model(
        gauge_groups=(SU3,),
        fields=(phi, Ag),
        lagrangian_decl=phi(c1, c2),
    ).lagrangian()

    delta = gauge_variation(group=SU3, parameter="alpha")
    results = apply_field_operator_to_term(L.terms[0], delta)

    assert len(results) == 2
    first_labels = results[0].fields[1].field.unpack_slot_labels(results[0].fields[1].labels)
    second_labels = results[1].fields[1].field.unpack_slot_labels(results[1].fields[1].labels)

    assert first_labels[1] == c2
    assert first_labels[0] != c1
    assert second_labels[0] == c1
    assert second_labels[1] != c2

    first_coupling = str(results[0].coupling)
    second_coupling = str(results[1].coupling)
    assert "c1" in first_coupling
    assert "c2" in second_coupling
    assert "t(" in first_coupling.lower()
    assert "t(" in second_coupling.lower()


def test_non_abelian_slot_policy_sum_still_requires_explicit_labels():
    """Each active repeated slot must carry a concrete label."""

    g, Ag, SU3 = _su3_sum_setup()
    phi = scalar_field(
        "PhiBi",
        self_conjugate=False,
        indices=(COLOR_FUND_INDEX, COLOR_FUND_INDEX),
    )

    L = InteractionTerm(
        coupling=Expression.num(1),
        fields=(phi.occurrence(labels={COLOR_FUND_INDEX.kind: (S("c1"), None)}),),
    )
    delta = gauge_variation(group=SU3, parameter="alpha")
    with pytest.raises(ValueError, match="representation slot 2"):
        apply_field_operator_to_term(L, delta)


