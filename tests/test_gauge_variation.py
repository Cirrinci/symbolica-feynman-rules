def test_non_abelian_gauge_boson_variation_has_inhomogeneous_and_structure_pieces():
    """The non-abelian gauge-boson variation splits as

        δ A^a_μ  =  + ∂_μ α^a  -  g f^{abc} α^b A^c_μ

    Verify, at the lowered-term level, that both summands are produced
    and that the inhomogeneous term carries a fresh ``∂_μ`` on α while
    the homogeneous term carries a structure constant ``f`` and an A
    factor.

    This is the structural building block used by the full pure-YM
    gauge-invariance argument. We assert structure rather than a full
    cancellation here because the YM cancellation between bilinear,
    cubic and quartic pieces of ``F^2`` is a global statement across
    many summands sharing lowered dummies, which is out of scope for
    the minimal first-cut tensor canonicaliser used here.
    """

    g, Ag, SU3 = _su3_setup()
    mu = S("mu")
    aa = S("aa")  # external adjoint label
    A_decl = Model(
        gauge_groups=(SU3,),
        fields=(Ag,),
        lagrangian_decl=Ag(mu, aa),
    ).lagrangian()
    assert len(A_decl.terms) == 1
    A_term = A_decl.terms[0]

    delta = gauge_variation(group=SU3, parameter="alpha")
    new_terms = apply_field_operator_to_term(A_term, delta)
    # Exactly two summands: inhomogeneous + homogeneous.
    assert len(new_terms) == 2

    # The first summand carries one fresh derivative ∂_μ on α(a).
    inhom, hom = new_terms
    assert len(inhom.derivatives) == 1
    assert inhom.derivatives[0].lorentz_index == mu
    assert inhom.fields[0].field.name == "alpha"

    # The second summand: replacement (alpha, A) with a structure constant
    # in the coupling.
    assert hom.derivatives == ()
    factor_names = [occ.field.name for occ in hom.fields]
    assert factor_names == ["alpha", "G"]
    coupling_str = str(hom.coupling)
    assert "f(" in coupling_str
    # And it carries the gauge coupling.
    assert "gS" in coupling_str


def test_pure_yang_mills_variation_is_well_formed():
    """Sanity check that applying ``gauge_variation`` to the **pure
    Yang-Mills Lagrangian** produces a finite, well-formed expression
    (no exceptions, no empty terms, alpha appears with a derivative).

    A full algebraic cancellation across the (bilinear + cubic + quartic)
    decomposition of ``F^2`` is *not* checked here for the reasons
    explained above; it is a known limitation of the minimal canonicaliser
    used in these unit tests.
    """

    g, Ag, SU3 = _su3_setup()
    mu, nu = S("mu"), S("nu")
    L = Model(
        gauge_groups=(SU3,),
        fields=(Ag,),
        lagrangian_decl=(
            -Expression.num(1) / Expression.num(4)
            * FieldStrength(SU3, mu, nu)
            * FieldStrength(SU3, mu, nu)
        ),
    ).lagrangian()

    delta = gauge_variation(group=SU3, parameter="alpha")
    dL = L.apply_operator(delta)

    # Every variation term must carry α somewhere.
    for term in dL.terms:
        names = [occ.field.name for occ in term.fields]
        assert "alpha" in names

    # The Symbolica export must include ``PartialD(alpha`` (the
    # inhomogeneous gauge-boson piece propagating through the F^2
    # derivatives).
    exported = dL.to_symbolica().expand()
    assert "PartialD(alpha" in str(exported)


def test_pure_yang_mills_variation_canonicalizes_to_zero_in_f_basis():
    """The fully expanded YM variation vanishes after the local tensor passes.

    The check intentionally keeps the color algebra in the explicit
    structure-constant basis so the Jacobi and antisymmetry reductions can act
    directly on ``f`` products.
    """

    g, Ag, SU3 = _su3_setup()
    mu, nu = S("mu"), S("nu")
    L = Model(
        gauge_groups=(SU3,),
        fields=(Ag,),
        lagrangian_decl=(
            -Expression.num(1) / Expression.num(4)
            * FieldStrength(SU3, mu, nu)
            * FieldStrength(SU3, mu, nu)
        ),
    ).lagrangian()

    delta = gauge_variation(group=SU3, parameter="alpha")
    exported = L.apply_operator(delta).to_symbolica().expand()
    lorentz, adjoint = _su3_pure_yang_mills_indices()

    canonical = canonize_full(
        exported,
        lorentz_indices=lorentz,
        adjoint_indices=adjoint,
        run_gamma=False,
        run_color=False,
    )
    assert canonical.to_canonical_string() == "0"


