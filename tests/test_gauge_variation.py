def _u1_setup():
    g = S("g")
    A = Field("A", spin=1, indices=(LORENTZ_INDEX,), self_conjugate=True)
    U1 = GaugeGroup(name="U1", abelian=True, coupling=g, charge="Q", gauge_boson=A)
    return g, A, U1


def test_u1_mass_term_is_gauge_invariant():
    """``m^2 Phibar Phi`` for a charged complex scalar is gauge-invariant
    independently of any derivatives or gauge field, so ``delta L = 0``
    exactly (no metric simplification needed).
    """

    g, A, U1 = _u1_setup()
    m2 = S("m2")
    phi = scalar_field("Phi", self_conjugate=False, quantum_numbers={"Q": 1})
    L = Model(
        gauge_groups=(U1,),
        fields=(phi, A),
        lagrangian_decl=m2 * phi.bar * phi,
    ).lagrangian()

    delta = gauge_variation(group=U1, parameter="alpha")
    dL = L.apply_operator(delta)
    assert dL.to_symbolica().expand().to_canonical_string() == "0"


def test_u1_mass_term_wrong_charge_is_not_gauge_invariant():
    """A *real* (self-conjugate) scalar cannot carry a U(1) charge: if we
    pretend otherwise, ``delta L`` is non-zero. This is the minimal "wrong
    charge gives nonzero variation" check.
    """

    g, A, U1 = _u1_setup()
    phi = scalar_field("Phi", self_conjugate=True, quantum_numbers={"Q": 1})
    L = Model(
        gauge_groups=(U1,),
        fields=(phi, A),
        lagrangian_decl=S("m2") * phi.bar * phi,
    ).lagrangian()

    delta = gauge_variation(group=U1, parameter="alpha")
    dL = L.apply_operator(delta)
    canonical = dL.to_symbolica().expand().to_canonical_string()
    assert canonical != "0"


def test_u1_scalar_kinetic_term_is_gauge_invariant():
    """``(D_mu Phi)^dagger (D^mu Phi)`` for a charged complex scalar.

    The lowered kinetic term has four pieces (kinetic, two trilinears with
    one ``A``, one quartic with two A's and a metric). The variation
    cancels only after Minkowski-metric contraction; ``canonize_full``
    takes care of that.
    """

    g, A, U1 = _u1_setup()
    mu = S("mu")
    phi = scalar_field("Phi", self_conjugate=False, quantum_numbers={"Q": 1})

    L = Model(
        gauge_groups=(U1,),
        fields=(phi, A),
        lagrangian_decl=CovD(phi.bar, mu) * CovD(phi, mu),
    ).lagrangian()

    delta = gauge_variation(group=U1, parameter="alpha")
    dL = L.apply_operator(delta)

    # The CovD lowering introduces an explicit Minkowski metric on the
    # ``A A`` quartic piece, so contract that first against the relevant
    # tensor heads. Lorentz dummies are ``mu`` (the bare kinetic label)
    # and ``nu`` (the metric's second slot).
    assert _zero(
        dL.to_symbolica(),
        lorentz=(mu, S("nu")),
        field_heads=(A,),
    )


def test_u1_fermion_kinetic_term_is_gauge_invariant():
    """QED kinetic ``i psibar gamma(mu) D_mu psi`` is gauge invariant.

    The two relevant dummy spinor labels are the ones emitted by the CovD
    lowering: ``i_bar_psi_U1`` / ``i_psi_U1`` for the gauge piece and
    ``i_bar_psi_covd`` / ``i_psi_covd`` for the bare-derivative piece.
    """

    g, A, U1 = _u1_setup()
    mu = S("mu")
    psi = dirac_field("psi", quantum_numbers={"Q": -1})

    L = Model(
        gauge_groups=(U1,),
        fields=(psi, A),
        lagrangian_decl=Expression.I * psi.bar() * Gamma(mu) * CovD(psi, mu),
    ).lagrangian()

    delta = gauge_variation(group=U1, parameter="alpha")
    dL = L.apply_operator(delta)
    assert _zero(
        dL.to_symbolica(),
        lorentz=(mu,),
        spinor=(
            S("i_bar_psi_U1"),
            S("i_psi_U1"),
            S("i_bar_psi_covd"),
            S("i_psi_covd"),
        ),
    )


def test_u1_yukawa_with_balanced_charges_is_gauge_invariant():
    """Yukawa ``y * Phi * eta.bar * eta`` is invariant iff the charges
    satisfy ``q_Phi + q_eta - q_eta = q_Phi = 0`` (since both ``eta`` slots
    appear, one with .bar). Here we take all three with zero charge - the
    minimal non-trivial Dirac+scalar coupling - and check explicitly.

    The non-trivial "balanced charges" case is captured by mass-type
    cancellations; this complement test guards against accidental sign
    bugs on the scalar slot when the charge is zero.
    """

    g, A, U1 = _u1_setup()
    phi = scalar_field("Phi", self_conjugate=False, quantum_numbers={"Q": 0})
    psi = dirac_field("psi", quantum_numbers={"Q": 0})

    L = Model(
        gauge_groups=(U1,),
        fields=(phi, psi, A),
        lagrangian_decl=S("y") * psi.bar() * psi() * phi,
    ).lagrangian()

    delta = gauge_variation(group=U1, parameter="alpha")
    dL = L.apply_operator(delta)
    assert dL.to_symbolica().expand().to_canonical_string() == "0"


def test_u1_yukawa_with_unbalanced_charges_is_not_invariant():
    """Pure ``y Phi psibar psi`` with ``q_Phi != 0`` (and equal q for
    ``psi`` and ``psibar``) does not satisfy charge conservation, so the
    variation is nonzero. This is the explicit "wrong-charge" check.
    """

    g, A, U1 = _u1_setup()
    phi = scalar_field("Phi", self_conjugate=False, quantum_numbers={"Q": 1})
    psi = dirac_field("psi", quantum_numbers={"Q": 0})

    L = Model(
        gauge_groups=(U1,),
        fields=(phi, psi, A),
        lagrangian_decl=S("y") * psi.bar() * psi() * phi,
    ).lagrangian()

    delta = gauge_variation(group=U1, parameter="alpha")
    dL = L.apply_operator(delta)
    canonical = canonize_full(dL.to_symbolica()).to_canonical_string()
    assert canonical != "0"


